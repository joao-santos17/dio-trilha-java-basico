public class exemploForArray {
    public static void main(String[] args) {

        //em arrays o índice inicia em zero
        String alunos [] = { "FELIPE", "JONAS", "JULIA", "MARCOS" };
        
        for (String aluno : alunos ){
            System.out.println("Nome do Aluno é: " + aluno);
        }
        
    }
}
